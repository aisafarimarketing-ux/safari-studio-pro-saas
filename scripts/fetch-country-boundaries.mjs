// One-shot script: fetch real country + major safari lake outlines
// from OSM/Nominatim, simplify aggressively, write static data
// modules under src/lib. Run with:
//   node scripts/fetch-country-boundaries.mjs
//
// Output is checked-in. Re-run only if a boundary changes
// significantly upstream (rare for countries).

import fs from "node:fs/promises";
import path from "node:path";

const COUNTRIES = [
  { key: "tanzania", q: "Tanzania" },
  { key: "kenya", q: "Kenya" },
  { key: "uganda", q: "Uganda" },
  { key: "rwanda", q: "Rwanda" },
  { key: "botswana", q: "Botswana" },
  { key: "zimbabwe", q: "Zimbabwe" },
  { key: "zambia", q: "Zambia" },
  { key: "southAfrica", q: "South Africa" },
];

const LAKES = [
  { key: "manyara", q: "Lake Manyara, Tanzania" },
  { key: "naivasha", q: "Lake Naivasha, Kenya" },
  { key: "nakuru", q: "Lake Nakuru, Kenya" },
  { key: "victoria", q: "Lake Victoria" },
  { key: "tanganyika", q: "Lake Tanganyika" },
];

const UA = "safari-studio-pro/1.0 (one-shot boundary fetch)";

async function fetchOne(q) {
  const url = `https://nominatim.openstreetmap.org/search?q=${encodeURIComponent(q)}&format=json&polygon_geojson=1&limit=1`;
  const res = await fetch(url, { headers: { "User-Agent": UA } });
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${q}`);
  const arr = await res.json();
  if (!arr.length) throw new Error(`no result for ${q}`);
  return arr[0];
}

// Ramer–Douglas–Peucker on [lng,lat] segments. Tolerance in degrees.
function rdp(points, tolerance) {
  if (points.length < 3) return points;
  let maxDist = 0;
  let index = 0;
  const [a, b] = [points[0], points[points.length - 1]];
  for (let i = 1; i < points.length - 1; i++) {
    const d = perpendicularDistance(points[i], a, b);
    if (d > maxDist) {
      maxDist = d;
      index = i;
    }
  }
  if (maxDist > tolerance) {
    const left = rdp(points.slice(0, index + 1), tolerance);
    const right = rdp(points.slice(index), tolerance);
    return [...left.slice(0, -1), ...right];
  }
  return [a, b];
}

function perpendicularDistance(p, a, b) {
  const [px, py] = p;
  const [ax, ay] = a;
  const [bx, by] = b;
  const num = Math.abs((by - ay) * px - (bx - ax) * py + bx * ay - by * ax);
  const den = Math.hypot(by - ay, bx - ax) || 1;
  return num / den;
}

function polygonArea(ring) {
  let area = 0;
  for (let i = 0; i < ring.length; i++) {
    const [x1, y1] = ring[i];
    const [x2, y2] = ring[(i + 1) % ring.length];
    area += x1 * y2 - x2 * y1;
  }
  return Math.abs(area / 2);
}

function simplify(geojson, tolerance) {
  // Closed-ring-aware RDP. For Polygon: take outer ring. For
  // MultiPolygon: take the largest polygon's outer ring (countries
  // with island annexes — we want the mainland).
  let ring = null;
  if (geojson.type === "Polygon") {
    ring = geojson.coordinates[0];
  } else if (geojson.type === "MultiPolygon") {
    let bestArea = 0;
    for (const poly of geojson.coordinates) {
      const r = poly[0];
      const a = polygonArea(r);
      if (a > bestArea) {
        bestArea = a;
        ring = r;
      }
    }
  }
  if (!ring) return [];
  // Strip closing point if duplicated.
  if (
    ring.length >= 2 &&
    ring[0][0] === ring[ring.length - 1][0] &&
    ring[0][1] === ring[ring.length - 1][1]
  ) {
    ring = ring.slice(0, -1);
  }
  // Split at the farthest point so RDP has a non-degenerate diameter.
  let farthest = 0;
  let farDist = 0;
  for (let i = 1; i < ring.length; i++) {
    const d = Math.hypot(ring[i][0] - ring[0][0], ring[i][1] - ring[0][1]);
    if (d > farDist) {
      farDist = d;
      farthest = i;
    }
  }
  const half1 = rdp(ring.slice(0, farthest + 1), tolerance);
  const half2 = rdp([...ring.slice(farthest), ring[0]], tolerance);
  return [...half1.slice(0, -1), ...half2.slice(0, -1)];
}

function toLatLng(ring) {
  // GeoJSON: [lng, lat] → Leaflet/our SVG: [lat, lng]
  return ring.map(([lng, lat]) => [
    Math.round(lat * 10000) / 10000,
    Math.round(lng * 10000) / 10000,
  ]);
}

function emitModule(label, items) {
  return (
    `// AUTO-GENERATED by scripts/fetch-country-boundaries.mjs.\n` +
    `// Source: OpenStreetMap via Nominatim (ODbL).\n\n` +
    `import type { LatLngTuple } from "leaflet";\n\n` +
    `export const ${label}: Record<string, LatLngTuple[]> = {\n` +
    items
      .map(
        (p) =>
          `  ${p.key}: [\n${p.coords
            .map(([lat, lng]) => `    [${lat}, ${lng}],`)
            .join("\n")}\n  ],`,
      )
      .join("\n") +
    `\n};\n`
  );
}

async function fetchSet(items, tolerance) {
  const out = [];
  for (const { key, q } of items) {
    process.stdout.write(`fetching ${key} (${q}) … `);
    try {
      const r = await fetchOne(q);
      if (!r.geojson) {
        console.log("no geojson");
        continue;
      }
      const ring = simplify(r.geojson, tolerance);
      const coords = toLatLng(ring);
      out.push({ key, coords });
      console.log(`${coords.length} verts`);
    } catch (err) {
      console.log(`err: ${err.message}`);
    }
    // Polite: ≤ 1 req/sec.
    await new Promise((r) => setTimeout(r, 1100));
  }
  return out;
}

async function main() {
  console.log("\n— countries —");
  const countries = await fetchSet(COUNTRIES, 0.06);
  await fs.writeFile(
    path.join("src", "lib", "countryBoundariesData.ts"),
    emitModule("COUNTRY_RINGS", countries),
  );

  console.log("\n— lakes —");
  // Lakes: smaller features, want more detail; tighter tolerance.
  // Lake Victoria is huge — use coarser tolerance for it specifically.
  const lakes = [];
  for (const item of LAKES) {
    const tol = item.key === "victoria" || item.key === "tanganyika" ? 0.05 : 0.005;
    process.stdout.write(`fetching ${item.key} (${item.q}) … `);
    try {
      const r = await fetchOne(item.q);
      if (!r.geojson) {
        console.log("no geojson");
        continue;
      }
      const ring = simplify(r.geojson, tol);
      const coords = toLatLng(ring);
      lakes.push({ key: item.key, coords });
      console.log(`${coords.length} verts`);
    } catch (err) {
      console.log(`err: ${err.message}`);
    }
    await new Promise((r) => setTimeout(r, 1100));
  }
  await fs.writeFile(
    path.join("src", "lib", "lakeBoundariesData.ts"),
    emitModule("LAKE_RINGS", lakes),
  );

  console.log(
    `\nwrote src/lib/countryBoundariesData.ts (${countries.length} countries) and src/lib/lakeBoundariesData.ts (${lakes.length} lakes)`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
